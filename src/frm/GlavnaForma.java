    private void btnPosaljiSvimaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPosaljiSvimaActionPerformed
        String txtPoruke = txtPorukaSvima.getText().trim();
        if (txtPoruke.length() > 200) {
            JOptionPane.showMessageDialog(this, "Poruka ne sme biti duza od 200 karaktera!");
            return;
        }

        if (!txtPoruke.isEmpty()) {
            Zahtev zahtev = new Zahtev();
            Poruka poruka = new Poruka();
            poruka.setPoruka(txtPoruke);
            poruka.setOdKorisnika(korisnik);
            poruka.setVreme(LocalDateTime.now());
            zahtev.setOperacija(Operacije.SLANJE_OPSTE_PORUKE);
            zahtev.putData("poruka", poruka);
            try {
                komunikacija.posalji(zahtev);
            } catch (IOException ex) {
                Logger.getLogger(GlavnaForma.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnPosaljiSvimaActionPerformed

    private void btnPosaljiPorukuSvima1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPosaljiPorukuSvima1ActionPerformed
        String txtPoruke = txtPorukaSvima1.getText().trim();
        if (txtPoruke.length() > 200) {
            JOptionPane.showMessageDialog(this, "Poruka ne sme biti duza od 200 karaktera!");
            return;
        }
        Korisnik odKorisnika = this.korisnik;
        Korisnik kaKorisniku = (Korisnik) cmbPrijavljeniKorisnici.getSelectedItem();
        System.out.println(kaKorisniku.getEmail());
        Poruka poruka=new Poruka();
        poruka.setKaKorisniku(kaKorisniku);
        poruka.setOdKorisnika(odKorisnika);
        poruka.setPoruka(txtPoruke);
        poruka.setVreme(LocalDateTime.now());
        Zahtev zahtev = new Zahtev();
        zahtev.setOperacija(Operacije.SLANJE_PORUKA_ODREDJENOM_KORISNIKU);
        zahtev.putData("poruka", poruka);
        try {
            komunikacija.posalji(zahtev);
        } catch (IOException ex) {
            Logger.getLogger(GlavnaForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnPosaljiPorukuSvima1ActionPerformed

