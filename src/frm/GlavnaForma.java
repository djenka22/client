    public void dodajKorisnika(Korisnik korisnik) {
        sviKorisnici.add(korisnik);
        cmbPrijavljeniKorisnici.removeAllItems();
        for (Korisnik k : sviKorisnici) {
            cmbPrijavljeniKorisnici.addItem(k);
        }

    }
    
     public void azurirajKorisnike(Odgovor odgovor) {
        List<Korisnik> korisnici = (List<Korisnik>) odgovor.getData("korisnici");
        cmbPrijavljeniKorisnici.removeAllItems();
        cmbSviKorisnici.removeAllItems();
        for (Korisnik k : korisnici) {

            cmbPrijavljeniKorisnici.addItem(k);
            cmbSviKorisnici.addItem(k);
        }
        cmbPrijavljeniKorisnici.removeAllItems();
        for (Korisnik k : korisnici) {
            cmbPrijavljeniKorisnici.addItem(k);
        }
    }
    
     private void prikaziPorukeOdOdredjenogKorisnika() {
        Korisnik korisnik=(Korisnik) cmbSviKorisnici.getSelectedItem();
        List<Poruka> porukeKorisnika = new ArrayList<>();
        for(Poruka p:((TableModelPoruke) tblPoslednje3poruke.getModel()).getSvePoruke()){
            if(p.getOdKorisnika().equals(korisnik)) porukeKorisnika.add(p);
            
        }
 
        ((TableModelPoruke) tblPorukeKorisnika.getModel()).setPorukeZaPrikaz(porukeKorisnika);
        ((TableModelPoruke) tblPorukeKorisnika.getModel()).fireTableDataChanged();
    }
    


